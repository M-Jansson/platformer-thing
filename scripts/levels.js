function level() {
	this.width;
	this.height;
	this.tileSize;

	this.spawnPoint;
	this.endPoint;

	this.coinCount = 0;
	this.totalCoins;

	this.completed = false;

	this.map = [];
}


function restartLevel(player, level) {
	player.vx = 0;
	player.vy = 0;
	for (i = 0; i < (level.width * level.height); i++) {
		if (level.map[i] < 0) {level.map[i] *= -1;}
		if (level.map[i] == 95) {
			player.x = (i % level.width + .25) * level.tileSize;
			player.y = (floor(i / level.width) + .25) * level.tileSize;
		}
	}
	level.coinCount = 0;
	player.onGround = false;
	player.extraJump = false;
}

bgColors = ["#EFE", "#FFE", "#FEE", "#EEE"];
function printLevel(level, levelIndex) {
	tiles = level.width * level.height;

	colorIndex = floor(levelIndex / 5);
	rectangle(0, 0, level.width * level.tileSize, level.height * level.tileSize, bgColors[colorIndex]);
	
	for (i = 0; i < tiles; i++) {
		tile_x = i % level.width * level.tileSize;
		tile_y = floor(i / level.width) * level.tileSize;

		//Tiles
		if (level.map[i] >= 1 && level.map[i] <= 15) {
			picture(tile_x, tile_y, level.tileSize + "/misc/brick.png");
		}

		//Platforms
		if (level.map[i] >= 16 && level.map[i] <= 31) {
			imgIndex_x = level.map[i] % 4;
			imgIndex_y = floor((level.map[i] - 16) / 4);
			imgID = level.tileSize + "/platforms/" + imgIndex_y + imgIndex_x + ".png";
			picture(tile_x, tile_y, imgID);
		}

		//Spikes
		if (level.map[i] >= 32 && level.map[i] <= 35) {
			picture(tile_x, tile_y, level.tileSize + "/spikes/" + level.map[i] % 4 + ".png");
		}
		
		//Gates & Buttons
		if (level.map[i] >= 36 && level.map[i] <= 59) {
			picture(tile_x, tile_y, level.tileSize + "/gates/" + floor((level.map[i] - 36) / 8) + "" + ((level.map[i] - 4) % 8) % 6 + ".png");
		}
		if (abs(level.map[i]) >= 60 && abs(level.map[i]) <= 71) {
			imgName = level.tileSize + "/buttons/" + floor((abs(level.map[i]) - 60) / 4) + "" + abs(level.map[i]) % 4;
			picture(tile_x, tile_y, imgName + "1.png");
			if (level.map[i] > 0) {
				picture(tile_x, tile_y, imgName + "0.png");
			}
		}
		
		//Misc
		if (level.map[i] == 96) {
			picture(tile_x, tile_y, level.tileSize + "/misc/chain.png");
		}
		if (level.map[i] == 97) {
			picture(tile_x, tile_y, level.tileSize + "/misc/gem.png");
		}
		if (level.map[i] == 98) {
			picture(tile_x, tile_y, level.tileSize + "/misc/coin.png");
		}
		if (level.map[i] == 99) {
			picture(tile_x, tile_y, level.tileSize + "/misc/door0.png");
			if (level.coinCount / level.totalCoins >= 1 || level.totalCoins == 0) {
				picture(tile_x, tile_y, level.tileSize + "/misc/door1.png");
			}
		}
	}
	
	text(level.width * level.tileSize + 10, 20, 16, "Level: " + (levelIndex + 1), "#000");
	text(level.width * level.tileSize + 10, 40, 16, "Coins: " + level.coinCount + "/" + level.totalCoins, "#000");
	text(level.width * level.tileSize + 10, 60, 16, "Deaths: " + player.deathCount, "#000");
}


